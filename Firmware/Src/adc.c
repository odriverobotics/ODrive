float read_ADC_volts(ADC_HandleTypeDef* hadc, uint8_t injected_rank) {
    uint32_t ADCValue;
    if(injected_rank) {
        ADCValue = HAL_ADCEx_InjectedGetValue(hadc, injected_rank);
    } else {
        ADCValue = HAL_ADC_GetValue(hadc);
    }
    return (3.3f/((float)(1<<12))) * ADCValue;
}

